# Python loops allow us to execute a statement or group of statements multiple times.

# ------------------------------------------------------------------------------------------------------------------------

# Types of loops in python
# -------------------------

# 1. while loop - Repeats a statement or group of statements while a given condition is TRUE. 
#                 It tests the condition before executing the loop body.

# 2. for loop - It's used for iterating over a sequence (such as a list, tuple, string, dictionary, or set) 
#               or other iterable objects. It allows a block of code to be executed for each item in the sequence. 

# 3. nested loops - You can use one or more loop inside any another while, for or do..while loop.

# ------------------------------------------------------------------------------------------------------------------------

# Python Loop Control Statements
# -------------------------------

# Loop control statements change execution from its normal sequence.

# 1. break statement - Terminates the loop statement and transfers execution to the statement immediately following the loop.

# 2. continue statement - The continue statement skips the rest of the current iteration of the loop and moves 
#                         the control to the beginning of the loop for the next iteration.

# 3. pass statement - The pass statement in Python is used when a statement is required syntactically but 
#                     you do not want any command or code to execute.

