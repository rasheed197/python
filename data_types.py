# Data types are used to define the types of a variable

# It defines what operations can be performed on a particular data

# Types of Data Types in Python
# 1. Numeric Data Types
#       a. int
#       b. float
#       c. complex
var1 = 1       # int data type
var3 = 10.023  # float data type
var4 = 10+3j   # complex data type

# 2. String Data Types
str = 'Hello World!'

# 3. Sequence Data Types
#       a. list
#       b. tuple
#       c. range
list = [ 'abcd', 786 , 2.23, 'john', 70.2 ]
tuple = ( 'abcd', 786 , 2.23, 'john', 70.2  )
range(10) # range(start, stop, step)

# 4. Binary Data Types
#       a. bytes
#       b. bytearray
#       c. memoryview

# 5. Dictionary Data Type
dict = {'name': 'john','code':6734, 'dept': 'sales'}

# 6. Set Data Type
#       a. set
#       b. frozenset
set = {'Java', 'Python', 'JavaScript'}

# 7. Boolean Data Type
a = True

# 8. None Type
x = None

# Primitive Data Types are the fundamental data types that are used to create 
# complex data types. They include -
# 1. Integers
# 2. Floats
# 3. Booleans, and
# 4. Strings

# Non-primitive Types store values or collections of values. They include - 
# 1. Lists
# 2. Tuples
# 3. Dictionaries, and
# 4. Sets